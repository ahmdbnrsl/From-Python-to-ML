# List

hobi = ["coding", "painting", "math"]
print(hobi[0])  # Output: coding
hobi.append("reading")  # Menambah elemen
print(hobi)  # Output: ['coding', 'painting', 'math', 'reading']

# Tuple

data = ("Ahmad", 25, 170.5)
print(data[1])  # Output: 25

# Dictionary

profil = {"nama": "Ahmad", "umur": 25, "tinggi": 170.5}
print(profil["nama"])  # Output: Ahmad
profil["pekerjaan"] = "Programmer"  # Menambah data
print(profil)

# Set

angka = {1, 2, 3, 3, 2}
print(angka)  # Output: {1, 2, 3}
