# variable and data type

string = "this is string"
integer = 10
decimal = 10.5
boolean = True or False

# print with concating string

print(string + " " + str(integer))

# print with F-String format

print(f"Macam-macam tipedata: {type(string)}, {type(integer)}, {type(decimal)}, {type(boolean)}")

# Aritmath operation

# Penjumlahan
print(10 + 5)  # Output: 15

# Pengurangan
print(10 - 5)  # Output: 5

# Perkalian
print(10 * 5)  # Output: 50

# Pembagian (hasil float)
print(10 / 3)  # Output: 3.333...

# Modulus (sisa bagi)
print(10 % 3)  # Output: 1

# Pangkat
print(2 ** 3)  # Output: 8

# Comparison operator

a = 10
b = 5

print(a > b)   # True
print(a < b)   # False
print(a == b)  # False
print(a != b)  # True

# Logic operator

is_student = True
is_coding = False

print(is_student and is_coding)  # False
print(is_student or is_coding)   # True
print(not is_student)            # False
